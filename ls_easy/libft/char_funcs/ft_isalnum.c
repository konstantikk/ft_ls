#include "libft.h"

int		ft_isalnum(int c)
{
	int	ex1;

	ex1 = (c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z');
	ex1 += (c >= '0' && c <= '9');
	return (ex1);
}
